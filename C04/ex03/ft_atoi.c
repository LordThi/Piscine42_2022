#include <unistd.h>
#include <stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	signs;
	int	nbr;

	i = 0;
	signs = 1;
	nbr = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			signs = signs * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = nbr * 10 + (str[i] - '0');
		i++;
	}
	return (nbr * signs);
}

int	main(void)
{
	char	str[] = " ---+--+1234ab567";

	printf("ceci est mon nombre : %d\n", ft_atoi(str));
	return(0);
}
